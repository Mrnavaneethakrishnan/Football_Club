package com.use;

public class Match {
	String title,place,dt;

	public Match(String title, String place, String dt) {
		super();
		this.title = title;
		this.place = place;
		this.dt = dt;
	}

	public String getTitle() {
		return title;
	}

	public String getPlace() {
		return place;
	}

	public String getDt() {
		return dt;
	}
	

}
